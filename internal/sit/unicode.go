func stringFromRoman(mac []byte) string {
	var buf []byte
	for _, roman := range mac {
		if roman < 128 {
			buf = append(buf, roman)
		} else {
			triple := u8list[roman-128]
			for triple != 0 {
				buf = append(buf, byte(triple))
				triple >>= 8
			}
		}
	}
	return string(buf)
}

